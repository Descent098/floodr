//! Core benchmark execution module.
//!
//! Handles running the full benchmark iteratively or concurrently, parsing tests,
//! creating HTTP connections, gathering stats, and writing reports.
//! 
//! ## Example
//! 
//! ### Execute from file
//! 
//! ```rust,ignore
//! use floodr::parsing::config::Config;
//! use floodr::engine::benchmark;
//! 
//! let config = Config::new("benchmark.yml", false, false, false, 1000, false, None, None);
//! let tags = &Tags::new(None, None);
//! 
//! benchmark::execute(
//!     "benchmark.yml".as_ref(), 
//!     None, 
//!     false, 
//!     config.no_check_certificate, 
//!     config.quiet, 
//!     Some(&config.timeout.to_string()), 
//!     config.verbose, 
//!     config.exec_terminal.as_deref(), 
//!     &tags, 
//!     None
//! );
//! ```
//! 
//! ### Manually Parse and execute plan with no Concurrency
//! 
//! ```rust, ignore
//! use floodr::parsing::config::Config;
//! use floodr::engine::benchmark;
//! use serde_yaml;
//! 
//! let config = Config::new("benchmark.yml", false, false, false, 1000, false, None, None);
//! 
//! let plan_data = r#"
//! - name: Fetch account
//!   request:
//!     url: /"#;
//!     
//! let plan_items = serde_yaml::from_str(plan_data).expect("Failed to parse");
//! println!("{:#?}", plan_items);
//!
//! let benchmark_plan = floodr::parsing::comparison_loader::load_from_items(plan_items);
//! benchmark::execute_from_plan(
//!     benchmark_plan,
//!     "http://localhost:4896".to_string(),
//!     config.relaxed_interpolations,
//!     config.no_check_certificate, 
//!     config.quiet,
//!     config.timeout,
//!     config.verbose,
//!     config.exec_terminal
//! );
//! ```
use std::collections::HashMap;
use std::sync::{Arc, Mutex};
use std::time::{Duration, Instant};

use futures::stream::{self, StreamExt};

use serde_json::{Map, Value, json};
use tokio::{runtime, time::sleep};

use crate::actions::{Report, Runnable};
use crate::parsing::config::Config;
use crate::expandable::include;
use crate::parsing::tags::Tags;
use crate::parsing::writer;

use reqwest::Client;

use colored::*;

/// Represents a single action item in a benchmark plan, storing both the
/// runnable logic and the source YAML node used to create it.
pub struct ActionItem {
  pub runnable: Box<dyn Runnable + Sync + Send>,
  pub source: serde_yaml::Value,
}

impl ActionItem {
  pub fn new(runnable: Box<dyn Runnable + Sync + Send>, source: serde_yaml::Value) -> Self {
    Self { runnable, source }
  }
}

/// Type alias for the runnable actions that make up a benchmark run
pub type Benchmark = Vec<ActionItem>;
/// Type alias for the benchmark execution context, storing variables and state.
pub type Context = Map<String, Value>;
/// Type alias for a collection of reports from a single iteration.
pub type Reports = Vec<Report>;
/// Type alias for the HTTP client connection pool.
pub type PoolStore = HashMap<String, Client>;
/// Type alias for a thread-safe connection pool.
pub type Pool = Arc<Mutex<PoolStore>>;

/// Represents the result of a full benchmark run.
///
/// # Fields
///
/// - `reports` (`Vec<Reports>`) - A vector containing a report list for each iteration.
/// - `duration` (`f64`) - The total time taken for all benchmark iterations in seconds.
pub struct BenchmarkResult {
  pub reports: Vec<Reports>,
  pub duration: f64,
}

/// Runs a single iteration of the benchmark plan.
///
/// This involves creating a new context, executing each action in the plan,
/// and collecting the resulting reports.
///
/// # Arguments
///
/// - `benchmark` (`Arc<Benchmark>`) - The shared execution plan.
/// - `pool` (`Pool`) - The client connection pool.
/// - `config` (`Arc<Config>`) - The shared benchmark configuration.
/// - `iteration` (`i64`) - The index of the current iteration.
///
/// # Returns
///
/// - `Vec<Report>` - The list of reports generated by this iteration.
async fn run_iteration(benchmark: Arc<Benchmark>, pool: Pool, config: Arc<Config>, iteration: i64) -> Vec<Report> {
  if config.rampup > 0 {
    let delay = config.rampup / config.iterations;
    sleep(Duration::new((delay * iteration) as u64, 0)).await;
  }

  let mut context: Context = Context::new();
  let mut reports: Vec<Report> = Vec::new();

  context.insert("iteration".to_string(), json!(iteration.to_string()));
  context.insert("base".to_string(), json!(config.base.to_string()));

  for item in benchmark.iter() {
    item.runnable.execute(&mut context, &mut reports, &pool, &config).await;
  }

  reports
}


/// Executes the full benchmark based on the provided parameters.
///
/// This sets up the configuration, initializes the execution plan (including
/// expanding nested includes), and runs iterations concurrently or sequentially
/// depending on the concurrency settings.
///
/// # Arguments
///
/// - `benchmark_path` (`&str`) - Path to the root benchmark YAML file.
/// - `report_path_option` (`Option<&str>`) - Optional path to save a result report.
/// - `relaxed_interpolations` (`bool`) - If true, missing variables won't cause a panic.
/// - `no_check_certificate` (`bool`) - If true, SSL certificate validation is disabled.
/// - `quiet` (`bool`) - If true, minimizes console output.
/// - `timeout` (`Option<&str>`) - Optional global request timeout in seconds.
/// - `verbose` (`bool`) - If true, enables detailed logging.
/// - `tags` (`&Tags`) - Tag filter for selecting which actions to include/skip.
///
/// # Returns
///
/// - `BenchmarkResult` - The aggregated results and duration of the run.
///
/// # Examples
///
/// ```rust,ignore
/// use floodr::parsing::config::Config;
/// use floodr::engine::benchmark;
/// 
/// let config = Config::new("benchmark.yml", false, false, false, 1000, false, None, None);
/// let tags = &Tags::new(None, None);
/// 
/// benchmark::execute(
///     "benchmark.yml".as_ref(), 
///     None, 
///     false, 
///     config.no_check_certificate, 
///     config.quiet, 
///     Some(&config.timeout.to_string()), 
///     config.verbose, 
///     config.exec_terminal.as_deref(), 
///     &tags, 
///     None
/// );
/// ```
#[allow(clippy::too_many_arguments)]
pub fn execute(benchmark_path: &str, report_path_option: Option<&str>, relaxed_interpolations: bool, no_check_certificate: bool, quiet: bool, timeout: Option<&str>, verbose: bool, exec_terminal: Option<&str>, tags: &Tags, base_override: Option<String>) -> BenchmarkResult {
  let config = Arc::new(Config::new(benchmark_path, relaxed_interpolations, no_check_certificate, quiet, timeout.map_or(10, |t| t.parse().unwrap_or(10)), verbose, exec_terminal.map(String::from), base_override));

  if report_path_option.is_some() {
    println!("{}: {}. Ignoring {} and {} properties...", "Report mode".yellow(), "on".purple(), "concurrency".yellow(), "iterations".yellow());
  } else {
    println!("{} {}", "Concurrency".yellow(), config.concurrency.to_string().purple());
    println!("{} {}", "Iterations".yellow(), config.iterations.to_string().purple());
    println!("{} {}", "Rampup".yellow(), config.rampup.to_string().purple());
  }

  println!("{} {}", "Base URL".yellow(), config.base.purple());
  println!();

  let threads = std::cmp::min(num_cpus::get(), config.concurrency as usize);
  let rt = runtime::Builder::new_current_thread().enable_all().worker_threads(threads).build().unwrap();

  rt.block_on(async {
    let mut benchmark: Benchmark = Benchmark::new();
    let pool_store: PoolStore = PoolStore::new();

    include::expand_from_filepath(benchmark_path, &mut benchmark, Some("plan"), tags);

    if benchmark.is_empty() {
      eprintln!("Empty benchmark. Exiting.");
      std::process::exit(1);
    }

    let benchmark = Arc::new(benchmark);
    let pool = Arc::new(Mutex::new(pool_store));

    if let Some(report_path) = report_path_option {
      let reports = run_iteration(benchmark.clone(), pool.clone(), config.clone(), 0).await;

      let report_data = json!({
        "base": config.base,
        "plan": benchmark.iter().map(|item| item.source.clone()).collect::<Vec<_>>(),
        "baseline": reports
      });

      writer::write_file(report_path, serde_yaml::to_string(&report_data).unwrap());

      BenchmarkResult {
        reports: vec![],
        duration: 0.0,
      }
    } else {
      let children = (0..config.iterations).map(|iteration| run_iteration(benchmark.clone(), pool.clone(), config.clone(), iteration));

      let buffered = stream::iter(children).buffer_unordered(config.concurrency as usize);

      let begin = Instant::now();
      let reports: Vec<Vec<Report>> = buffered.collect::<Vec<_>>().await;
      let duration = begin.elapsed().as_secs_f64();

      BenchmarkResult {
        reports,
        duration,
      }
    }
  })
}

/// Executes a benchmark plan that has already been loaded (e.g., from a report file).
///
/// # Arguments
///
/// - `benchmark` (`Benchmark`) - The pre-constructed execution plan.
/// - `base` (`String`) - The base URL to use.
/// - `relaxed_interpolations` (`bool`) - If true, missing variables won't cause a panic.
/// - `no_check_certificate` (`bool`) - If true, SSL certificate validation is disabled.
/// - `quiet` (`bool`) - If true, minimizes console output.
/// - `timeout` (`u64`) - Request timeout in seconds.
/// - `verbose` (`bool`) - If true, enables detailed logging.
/// - `exec_terminal` (`Option<String>`) - Optional terminal override.
///
/// # Returns
///
/// - `BenchmarkResult` - The results and duration of the run.
/// 
/// # Example
/// 
/// ```rust, ignore
/// use floodr::parsing::config::Config;
/// use floodr::engine::benchmark;
/// use serde_yaml;
/// 
/// let config = Config::new("benchmark.yml", false, false, false, 1000, false, None, None);
/// 
/// let plan_data = r#"
/// - name: Fetch account
///   request:
///     url: /"#;
///     
/// let plan_items = serde_yaml::from_str(plan_data).expect("Failed to parse");
/// println!("{:#?}", plan_items);
///
/// let benchmark_plan = floodr::parsing::comparison_loader::load_from_items(plan_items);
/// benchmark::execute_from_plan(
///     benchmark_plan,
///     "http://localhost:4896".to_string(),
///     config.relaxed_interpolations,
///     config.no_check_certificate, 
///     config.quiet,
///     config.timeout,
///     config.verbose,
///     config.exec_terminal
/// );
/// ```
#[allow(clippy::too_many_arguments)]
pub fn execute_from_plan(benchmark: Benchmark, base: String, relaxed_interpolations: bool, no_check_certificate: bool, quiet: bool, timeout: u64, verbose: bool, exec_terminal: Option<String>) -> BenchmarkResult {
  let config = Arc::new(Config {
    base,
    concurrency: 1,
    iterations: 1,
    relaxed_interpolations,
    no_check_certificate,
    rampup: 0,
    quiet,
    timeout,
    verbose,
    exec_terminal,
  });

  let rt = runtime::Builder::new_current_thread().enable_all().worker_threads(1).build().unwrap();

  rt.block_on(async {
    let pool_store: PoolStore = PoolStore::new();
    let benchmark = Arc::new(benchmark);
    let pool = Arc::new(Mutex::new(pool_store));

    let begin = Instant::now();
    let reports = run_iteration(benchmark.clone(), pool.clone(), config.clone(), 0).await;
    let duration = begin.elapsed().as_secs_f64();

    BenchmarkResult {
      reports: vec![reports],
      duration,
    }
  })
}
